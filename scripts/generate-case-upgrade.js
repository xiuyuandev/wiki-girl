// ============ 渲染函数 ============
function esc(value) {
  return String(value == null ? '' : value)
    .replace(/&/g, '&amp;')
    .replace(/</g, '&lt;')
    .replace(/>/g, '&gt;')
    .replace(/"/g, '&quot;')
    .replace(/'/g, '&#39;');
}

function renderLabels(items, className) {
  return items.map(function (item) { return '<span class="' + className + '">' + esc(item) + '</span>'; }).join('');
}

